#ifndef COLORS_H
#define COLORS_H

// Reset
#define RESET "\033[0m"  // Reset to default color

// Regular Colors
#define BLACK   "\033[0;30m"  // Black
#define RED     "\033[0;31m"  // Red
#define GREEN   "\033[0;32m"  // Green
#define YELLOW  "\033[0;33m"  // Yellow
#define BLUE    "\033[0;34m"  // Blue
#define MAGENTA "\033[0;35m"  // Magenta
#define CYAN    "\033[0;36m"  // Cyan
#define WHITE   "\033[0;37m"  // White

// Bold Colors
#define BOLD_BLACK   "\033[1;30m"  // Bold Black
#define BOLD_RED     "\033[1;31m"  // Bold Red
#define BOLD_GREEN   "\033[1;32m"  // Bold Green
#define BOLD_YELLOW  "\033[1;33m"  // Bold Yellow
#define BOLD_BLUE    "\033[1;34m"  // Bold Blue
#define BOLD_MAGENTA "\033[1;35m"  // Bold Magenta
#define BOLD_CYAN    "\033[1;36m"  // Bold Cyan
#define BOLD_WHITE   "\033[1;37m"  // Bold White

// Underline Colors
#define UNDERLINE_BLACK   "\033[4;30m"  // Underline Black
#define UNDERLINE_RED     "\033[4;31m"  // Underline Red
#define UNDERLINE_GREEN   "\033[4;32m"  // Underline Green
#define UNDERLINE_YELLOW  "\033[4;33m"  // Underline Yellow
#define UNDERLINE_BLUE    "\033[4;34m"  // Underline Blue
#define UNDERLINE_MAGENTA "\033[4;35m"  // Underline Magenta
#define UNDERLINE_CYAN    "\033[4;36m"  // Underline Cyan
#define UNDERLINE_WHITE   "\033[4;37m"  // Underline White

// Background Colors
#define BG_BLACK   "\033[40m"  // Black Background
#define BG_RED     "\033[41m"  // Red Background
#define BG_GREEN   "\033[42m"  // Green Background
#define BG_YELLOW  "\033[43m"  // Yellow Background
#define BG_BLUE    "\033[44m"  // Blue Background
#define BG_MAGENTA "\033[45m"  // Magenta Background
#define BG_CYAN    "\033[46m"  // Cyan Background
#define BG_WHITE   "\033[47m"  // White Background

#endif  // COLORS_H
